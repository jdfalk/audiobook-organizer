#!/bin/bash
# file: scripts/setup-git-hooks.sh
# version: 1.0.0
# Set up git pre-commit hook to prevent accidentally committing .api-token

HOOK_DIR=".git/hooks"
HOOK_FILE="$HOOK_DIR/pre-commit"

# Create hooks directory if it doesn't exist
mkdir -p "$HOOK_DIR"

# Create pre-commit hook
cat > "$HOOK_FILE" <<'EOF'
#!/bin/bash
# Pre-commit hook: prevent .api-token from being committed

PROTECTED_FILES=(
    ".api-token"
    ".claude/.api-token"
    ".bootstrap-token"
    ".readonly-key"
)

STAGED_FILES=$(git diff --cached --name-only)

for FILE in "${PROTECTED_FILES[@]}"; do
    if echo "$STAGED_FILES" | grep -q "^$FILE$"; then
        echo "❌ Error: Attempting to commit protected file: $FILE"
        echo ""
        echo "This file contains secrets and should never be committed."
        echo "It's in .gitignore and is auto-generated by scripts."
        echo ""
        echo "To unstage this file:"
        echo "  git reset HEAD $FILE"
        echo ""
        exit 1
    fi
done

exit 0
EOF

# Make hook executable
chmod +x "$HOOK_FILE"

echo "✅ Git pre-commit hook installed at $HOOK_FILE"
echo ""
echo "Protected files and directories:"
echo "  - .api-token (shared API key across worktrees)"
echo "  - .claude/.api-token (same as above)"
echo "  - .bootstrap-token (temporary bootstrap auth token)"
echo "  - .readonly-key (startup read-only API key)"
echo "  - .claude/.credentials/ (per-worktree username/password)"
echo ""
echo "The hook will prevent these from being committed."
echo "If you accidentally stage one, you'll get an error message."
echo ""
echo "To manage worktree credentials, use:"
echo "  ./scripts/manage-credentials.sh --help"
